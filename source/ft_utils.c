#include "../include/philo.h"

int	ft_atoi(char *s)
{
	int		i;
	int		res;

	res = 0;
	i = 0;
	while (s[i] == ' ' || s[i] == '\t')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
		res = res * 10 + (s[i++] - '0');
	return (res);
}
